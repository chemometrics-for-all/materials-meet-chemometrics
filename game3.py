c2.markdown(f'<div class="metric">Accuracy<br>{accuracy:.1f}%</div>', unsafe_allow_html=True)
c3.markdown(f'<div class="metric">Streak 🔥<br>{st.session_state.streak}</div>', unsafe_allow_html=True)

