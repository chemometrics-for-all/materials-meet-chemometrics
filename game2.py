fig, ax = plt.subplots()
ax.plot(x, y, linewidth=2)

ax.set_facecolor("#0f172a")
fig.patch.set_facecolor("#0f172a")

ax.set_xlabel("Wavelength", color="white")
ax.set_ylabel("Intensity", color="white")
ax.tick_params(colors='white')

st.pyplot(fig)
st.markdown('</div>', unsafe_allow_html=True)

# -----------------------------
# USER INPUT
# -----------------------------
st.markdown("### 🧠 Your Decision")

choice = st.radio(
    "Is this spectrum NORMAL?",
    ["Normal", "Not Normal"],
    horizontal=True
)

col1, col2 = st.columns(2)

with col1:
    validate = st.button("✔ Validate")

with col2:
    next_case = st.button("➡ Next")

# -----------------------------
# VALIDATION
# -----------------------------
if validate:
    st.session_state.total += 1

    correct = (
        (choice == "Normal" and is_normal) or
        (choice == "Not Normal" and not is_normal)
    )

    if correct:
        st.success("✨ Perfect! You are training your eye.")
        st.session_state.score += 1
        st.session_state.streak += 1
    else:
        st.error("⚠ Not correct")

        if is_normal:
            st.info("👉 This is NORMAL: smooth + stable + clear peaks")
        else:
            st.info("👉 This is NOT normal: something disturbs the signal")

        st.session_state.streak = 0

    st.caption(f"🔍 Hidden case: {explanation}")

# -----------------------------
# NEXT CASE
# -----------------------------
if next_case:
    st.session_state.index += 1

    if st.session_state.index >= len(cases):
        st.session_state.index = 0

    st.rerun()

# -----------------------------
# METRICS
# -----------------------------
