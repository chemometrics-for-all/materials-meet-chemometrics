# -----------------------------
# GENERATE 20 CASES
# -----------------------------
def generate_cases():
    x = np.linspace(0, 100, 500)

    base = (
        np.exp(-(x - 30)**2 / 50)
        + 0.7 * np.exp(-(x - 60)**2 / 80)
    )

    cases = []

    # -------- NORMAL (5) --------
    cases.append((base, True, "Clean spectrum"))
    cases.append((base * 1.1, True, "Higher intensity"))
    cases.append((np.exp(-(x - 40)**2 / 60), True, "Single peak"))
    cases.append((base + 0.02*np.random.randn(len(x)), True, "Very low noise"))
    cases.append((base * 0.8, True, "Lower intensity"))

    # -------- DISTORTED (15) --------
    cases.append((base + np.random.normal(0, 0.2, len(x)), False, "High noise"))
    cases.append((base + np.random.normal(0, 0.4, len(x)), False, "Extreme noise"))
    cases.append((base + 0.3*(x/max(x)), False, "Baseline drift"))
    cases.append((base + 0.6*(x/max(x)), False, "Strong drift"))
    cases.append((base - 0.3*(x/max(x)), False, "Negative drift"))

    shift = np.exp(-(x - 40)**2 / 50) + 0.7*np.exp(-(x - 70)**2 / 80)
    cases.append((shift, False, "Peak shift"))

    cases.append((base * 0.3, False, "Flattened peaks"))
    cases.append((np.clip(base * 3, 0, 1), False, "Saturation"))
    cases.append((base + 0.1*np.sin(x/5), False, "Wavy baseline"))

    spikes = base.copy()
    spikes[::20] += 0.5
    cases.append((spikes, False, "Spikes"))

    cases.append((base + 0.5, False, "Offset baseline"))
    cases.append((base * (1 + 0.3*np.random.randn(len(x))), False, "Multiplicative noise"))

    broad = np.exp(-(x - 30)**2 / 150)
    cases.append((broad, False, "Peak broadening"))

    weird = base + 0.2*np.sin(x)
    cases.append((weird, False, "Shape distortion"))

    mixed = base + 0.2*np.random.randn(len(x)) + 0.3*(x/max(x))
    cases.append((mixed, False, "Mixed distortion"))

    return x, cases

x, cases = generate_cases()
y, is_normal, explanation = cases[st.session_state.index]

# -----------------------------
# PLOT
# -----------------------------
